int count =0;
int Stepping_Rate_Frequency =0;
int Steps =0;

/*
 * Configurations:
 * Enable Timer 3 and Timer 6
 * Timer will generate PWM Pulses and Timer 6 will count the number of PWM pulses as an Interrupt
 */


void rotate(int RPM, int Step, uint8_t Direction){
	int arr=0, psc=1;

	if(Direction == 'C'){	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, 1);}
	if(Direction == 'A'){	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9,0);}

	Steps = Step;
	arr = (14400000/ RPM);

	if( arr > 65535){
		if((arr /2 <=  65535)){
			arr = arr/2;
			psc = 2;
		}
		else if ((arr /4 <= 65535)){
			arr = arr/4;
			psc = 4;
		}
		else if ((arr/ 8 <= 65535)){
			arr = arr/8;
			psc = 8;
		}
	}
	TIM3->ARR = arr-1;
	TIM3->PSC = psc-1;
	TIM6->ARR = arr-1;
	TIM6->PSC = psc-1;

	HAL_TIM_Base_Start(&htim3);
	HAL_TIM_Base_Start_IT(&htim6);
	HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
}

/*
	 Logic:

		1 min - 200 rotations
		1 sec - 200 / 60;

		Stpper Motor Calculations:

		Stepping Rate = (Step Counts/ 60) * Desired RPM		// Step Counts = 200
				  = (200/60)* 200				// RPM = 200
				  = 666.67 Hz

		Stepping Time = 1/Stepping Rate = 1.5 msecs			// delay = 1.5 * 10^(-3) inbtw 2 PWM pulses

		=> 200 RPM  == 666.67 Hz == or 1.5 msec PWM pulses
		for x RPM   == (666.67/x)*200 == or x/ (1.5 msec * 200)  PWM Pulses = delay

		Timer formula: (ARR + 1)*(PSC + 1) = (frequency)*(delay)*(CNT + 1)

		(ARR + 1)*(PSC + 1) = ((frequency)*(delay)*200) / (Required RPM)
		delay = ((48 * 10^6) * (1.5 * 10*(-3)) * 200) / (Required RPM)
			  = 14400000/ RPM

	 */

int main(void)
{
	HAL_Init();
	SystemClock_Config();
	MX_GPIO_Init();
	MX_USART2_UART_Init();
	MX_TIM3_Init();
	MX_TIM6_Init();

	rotate(60, (200*500), 'C');		// (RPM, (Full Steps * No_of_Rotations), Direction)

  while (1){
	  if(count == (Steps)){
		  HAL_TIM_Base_Stop_IT(&htim6);
		  HAL_TIM_PWM_Stop(&htim3, TIM_CHANNEL_1);
		  HAL_TIM_Base_Stop(&htim3);
	  }
  }
}
